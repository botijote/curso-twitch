package org.rgil.sesion12;

public class ImprimirInteger {

    private int entero;

    public ImprimirInteger() {
        this.entero = 0;
    }
    public ImprimirInteger(int entero) {
        this.entero = entero;
    }

    public void imprime(){
        System.out.println(this.entero);
    }

}
